    private void txtUserFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtUserFocusLost
        // TODO add your handling code here:
        String user=UserDAO.getUserName(txtUser.getText());
        if(user!=null){
            user.replaceAll(" ","");
            user.trim();
            //JOptionPane.showMessageDialog(this,user+"\n"+user.length());
            if(txtUser.getText().equals(user))
            //if(UserDAO.isUserNameExist(txtUser.getText()))
            {
                lblAccount.setVisible(true);
                txtUser.selectAll();
                txtUser.requestFocus();
            }
            else
                lblAccount.setVisible(false);
        }
    }//GEN-LAST:event_txtUserFocusLost

    private void txtUserKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtUserKeyPressed
        // TODO add your handling code here:
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) { 
            String user=UserDAO.getUserName(txtUser.getText());
            if(user!=null){
                user.replaceAll(" ","");
                user.trim();
                //JOptionPane.showMessageDialog(this,user+"\n"+user.length());
                if(txtUser.getText().equals(user))
                //if(UserDAO.isUserNameExist(txtUser.getText()))
                {
                    lblAccount.setVisible(true);
                    txtUser.selectAll();
                    txtUser.requestFocus();
                }
            }
            else
            {
                lblAccount.setVisible(false);
                txtName.requestFocus();
            }
        }
    }//GEN-LAST:event_txtUserKeyPressed

