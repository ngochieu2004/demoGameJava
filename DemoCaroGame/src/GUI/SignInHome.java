    private void txtRePassFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtRePassFocusLost
        // TODO add your handling code here:
        if(Arrays.equals(txtPass.getPassword(),txtRePass.getPassword()))
        {
            lblRePass.setVisible(false);
        }
        else
        {
            txtPass.selectAll();
            txtRePass.requestFocus();
            lblRePass.setVisible(true);
        }
    }//GEN-LAST:event_txtRePassFocusLost

    private void txtPassFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtPassFocusLost
        // TODO add your handling code here:
        if(txtPass.getPassword().length<6)
        {
            txtPass.selectAll();
            txtPass.requestFocus();
            lblPass.setVisible(true);
        }
        else
            lblPass.setVisible(false);
    }//GEN-LAST:event_txtPassFocusLost

    private void btnSignInActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSignInActionPerformed
        // TODO add your handling code here:
        boolean ketqua = UserDAO.addUser(txtUser.getText(),txtPass.getText(),txtName.getText());
        if(ketqua==true){
        JOptionPane.showMessageDialog(this,"Them thanh cong!","Thong bao",JOptionPane.INFORMATION_MESSAGE);
        this.setVisible(false);
        }
        else if(txtName.getText().isEmpty()||txtPass.getText().isEmpty()||txtUser.getText().isEmpty()||txtRePass.getText().isEmpty())
            JOptionPane.showMessageDialog(this,"Vui long nhap day du thong tin","Thong bao", JOptionPane.INFORMATION_MESSAGE);
        else
            JOptionPane.showMessageDialog(this,"Tao tai khoan that bai", "Thong bao",JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_btnSignInActionPerformed

